C**** SPINIT()
c
      subroutine spinit()
c
c-----------------------------------------------------------------------
c
c    displays a little "clock" thing to measure progress.
c
c-----------------------------------------------------------------------
c    Include files:
c-----------------------------------------------------------------------
c
      include 'nonrdprm.inc'
      include 'nonrdio.inc'
c
c-----------------------------------------------------------------------
c   Entry point:
c-----------------------------------------------------------------------
c
cgwilson      icall = MOD(icall+1,8)
cgwilson      write(IOWSTD,9000,ERR=9999) spin(icall)
cgwilson      call flush(IOWSTD)
c
c-----------------------------------------------------------------------
c   Format statements:
c-----------------------------------------------------------------------
c
 9000 format('+','Initializing ...',A)
c
c
c-----------------------------------------------------------------------
c   Return point:
c-----------------------------------------------------------------------
c
 9999 continue
      return
      end
